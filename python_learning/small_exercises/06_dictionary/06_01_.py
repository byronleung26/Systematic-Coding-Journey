# 使用一个字典来存储一个人的信息，包括名、姓、年龄和居住的城市。
# 该字典应包含键first_name、last_name、age和city。将存储在该字典中的每项信息都打印出来。
information = {"first_name": "Byran", "last_name": "Leung", "age": 28, "city": "WuHan"}
print(information["first_name"])
print(information["last_name"])
print(information["age"])
print(information["city"])